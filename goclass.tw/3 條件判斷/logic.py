# 接收使用者輸入
a, b = int(input()), int(input())
# for迴圈: 變數i將會變成 串列["<", "<=", ">", ">=", "==", "!="] 中的值
# 其實我也講不清楚，所以直接上運作狀況
# 第一次 => i = "<"(串列的第0項)
# 第二次 => i = "<="(串列的第1項)
# 第三次 => i = ">"(串列的第2項)
# 第四次 => i = ">="(串列的第3項)
# 第五次 => i = "=="(串列的第4項)
# 第六次 => i = "!="(串列的第5項)
# 串列裡面有6個值，所以for迴圈會跑6次
for i in ["<", "<=", ">", ">=", "==", "!="]:
    print(f"{a} {i} {b} {str(eval(f'a {i} b'))}")
    # 輸出
    # 透過eval()函式可以將輸入的字串作為程式執行的特性
    # 將a, b邏輯運算的結果(布林值)轉換為字串輸出